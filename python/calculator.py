a=int(input("enter your first number"))
b=int(input("enter your second number"))
opr=input("enter your operation")
if opr=="+":
    print(a+b)
elif opr=="-":
    print(a-b)
elif opr=="*":
    print(a*b)
elif opr=="/":
    print(a/b)
elif opr=="%":
    print(a%b)
elif opr=="//":
    print(a//b)
elif opr=="**":
    print(a**b)